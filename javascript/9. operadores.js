//Operadores lógicos

let nome1 = 'Marta'
let nome2 = 'Marta'

let idade1 = 10
let idade2 = "10"


//OPERADORES DE COMPARAÇÃO

console.log("\nIGUAL")
console.log(nome1 === nome2)
//três sinais de ' = ' indica que é o mesmo valor/conteúdo e mesmo tipo

console.log(idade1 == idade2)
//dois sinai de ' = ' indica que é o mesmo valor/conteído

console.log(idade1 === idade2)
//três sinais de ' = ' indica que é o mesmo valor/conteúdo e mesmo tipo


console.log("\nDIFERENTE")
console.log(nome1 != nome2)
//falso pq são iguais, possuem o mesmo valor e o mesmo tipo

console.log(idade1 !== idade2)
//verdadeiro pq são diferentes, possuem o mesmo valor MAS são de tipos diferentes


console.log("\nAND")
console.log(1 < 2 && 1 < 3)
console.log(10 < 2 && 10 < 3)


console.log("\nOR")
console.log(10 < 2 || 10 < 3)