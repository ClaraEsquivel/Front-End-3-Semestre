let a = 2
let b = 5
let c_resultado

if (a == b){
    c_resultado = a + b
    console.log("Números iguais: Soma")
    console.log(`Resultado: ${a} + ${b} = ${c_resultado}`)
} else{
    c_resultado = a * b
    console.log("Números diferentes: Multiplicação")
    console.log(`Resultado: ${a} * ${b} = ${c_resultado}`)
}


